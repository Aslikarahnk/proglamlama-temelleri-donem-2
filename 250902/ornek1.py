#1. indeksden 4. indekse kadar olan listeyi oluşturun 4. dahil değil
asal_sayilar=[2,3,5,7,11,13,17,19,23]#liste oldu
print(asal_sayilar[1:4])#3 5 7

#5. indeksden sona kadar tum liste elemanları yaz
asal_sayilar=[2,3,5,7,11,13,17,19,23]#liste oldu
print(asal_sayilar[5:])#13 17 19 23

#listenin başından itibaren 5. elemana kadar olanları yaz
asal_sayilar=[2,3,5,7,11,13,17,19,23]#liste oldu
print(asal_sayilar[:5])#2 5 11

#0 dan 6. indekse kadar 2şer 2şer yaz
asal_sayilar=[2,3,5,7,11,13,17,19,23]#liste oldu
print(asal_sayilar[2:5:11])#2,5,11